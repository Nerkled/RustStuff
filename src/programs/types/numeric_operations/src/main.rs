// Numeric Operations 4/24/2024
fn main() {
    // addition
    let sum = 5+10;

    // subtraction
    let difference = 95.5 - 4.3;

    // multiplication 
    let product = 4 * 30; 

    // division 
    let quotient = 56.7 / 32.2;
    let truncated = -5 / 3; // Results in -1, as it truncates the decimal part

    // remainder 
    let remainder = 43 % 5;

    println!("sum: {sum}");
    println!("difference: {difference}");
    println!("product: {product}");
    println!("quotient: {quotient}");
    println!("truncated: {truncated}");
    println!("remainder: {remainder}");
}
